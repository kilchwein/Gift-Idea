// Function to get random questions for each session
function getRandomQuestions(count = 10) {
    const categories = Object.keys(questionBank);
    let selectedQuestions = [];
    
    // Ensure at least one question from each category
    categories.forEach(category => {
        const randomIndex = Math.floor(Math.random() * questionBank[category].length);
        selectedQuestions.push({
            ...questionBank[category][randomIndex],
            category
        });
    });
    
    // Fill remaining slots with random questions
    while (selectedQuestions.length < count) {
        const randomCategory = categories[Math.floor(Math.random() * categories.length)];
        const randomQuestion = questionBank[randomCategory][Math.floor(Math.random() * questionBank[randomCategory].length)];
        
        // Avoid duplicates
        if (!selectedQuestions.find(q => q.text === randomQuestion.text)) {
            selectedQuestions.push({
                ...randomQuestion,
                category: randomCategory
            });
        }
    }
    
    // Shuffle the questions
    return selectedQuestions.sort(() => Math.random() - 0.5);
}

